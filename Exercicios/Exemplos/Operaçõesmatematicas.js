var a = Number(prompt("Digite o primeiro número:"));
var b = Number(prompt("Digite o segundo número:"));
 document.write (`<p> A soma entre: ${a} + ${b} é igual a: ${a + b}.</p>`);
 document.write (`<p> O multiplicação entre: ${a} X  ${b} é igual a: ${a * b}</p>` );
 document.write (`<p> A subtração entre: ${a} - ${b} é igual a: ${a - b}`)
 document.write (`<p> A divisão entre: ${a} / ${b} é igual a: ${a / b}</p>`)
 document.write (`<p> O resto da divisão entre: ${a} / ${b} é igual a: ${a % b}</p>`)
 document.write (`<p>A potência de ${a} elevado a ${b} é igual a: ${a ** b}</p>`)